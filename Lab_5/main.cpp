void Deikstra(int graf[][SIZE])
{
    int TSD[SIZE];
    TSD[0] = TSD[1] = 0;

    for (int i = 2; i < SIZE; i++)
        TSD[i] = 999999;

    int distance;

    for (int i = 1; i < SIZE; i++)
    {
        for (int j = 1; j < SIZE; j++)
        {
            distance = TSD[i] + graf[i][j];

            if (graf[i][j] != 0 && distance < TSD[j])
                TSD[j] = distance;
        }
    }

    cout << "\n -> The shortest distance between V and V* - " << TSD[SIZE - 1] << endl;
    cout << "\n -> The shortest distance from V to any vertex: \n\t";
    for (int i = 1; i < SIZE; i++)
        cout << TSD[i] << "  ";

    int ver[SIZE];
    int end = 30;
    ver[0] = end;
    int k = 1;
    int weight = TSD[end];


    while (end != 1)
    {
        for (int i = 1; i < SIZE; i++)
            if (graf[end][i] != 0)
            {
                int temp = weight - graf[end][i];
                if (temp == TSD[i])
                {
                    weight = temp;
                    end = i;
                    ver[k] = i;
                    k++;
                }
            }
    }

    cout << "\n\n ->The shortest path between V and V*:\n\t";
    for (int i = k - 1; i >= 0; i--)
    {
        if (i != 0)
            cout << ver[i] << " => ";
        else
            cout << ver[i];
    }
}